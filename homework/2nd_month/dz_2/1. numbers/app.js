let input1 = Number(prompt("Введите число 1."))
let input2 = Number(prompt("Введите число 2."))

if (input1 > input2) {
    console.log("Первое число больше.");

} else if (input1 < input2) {
    console.log("Второе число больше.");

} else if (input1 === input2) {
    console.log("Числа равны.");
}




